     <p> I used this most recently to take my largest panorama yet, <a href="http://ka.rljohnson.net/photos/Panoramas/20220912/#zoom=1&x=0.5&y=0.15076598073471387">a 20 gigapixel shot from Mount Muscoco</a>. The 1600 photos needed for this one took around 20 minutes of acquisition time or something like that. Unfortunately, because I took all the photos wide open (quite unusual for me), the vignetting was quite bad and I wasn't able to fix it all. Additionally, the sharpness is a lot worse than this lens is capable of, both due to softness from the teleconverter and depth of field issues. Looking forward to hike back up there next time I'm in Colorado and have a proper do-over. </p><br>
